package Calculator;

public class MathOperations {
    public static int Sum(int a, int b){
        return a+b;
    }

    public static int Sub(int a, int b){
        return a-b;
    }

    public static int Multiplication(int a, int b){
        return a*b;
    }

    public static int Division(int a, int b){
        return a/b;
    }
}